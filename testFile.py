from Stack import Stack
from lab04 import solveMaze

def test_solvable_maze():
    maze = [
        ['+', '+', '+', '+', 'G', '+'],
        ['+', ' ', '+', ' ', ' ', '+'],
        ['+', ' ', ' ', ' ', '+', '+'],
        ['+', ' ', '+', '+', ' ', '+'],
        ['+', ' ', ' ', ' ', ' ', '+'],
        ['+', '+', '+', '+', '+', '+']
    ]
    
    assert solveMaze(maze, 4, 4) == True

    assert maze == [
        ['+', '+', '+', '+', 'G', '+'],
        ['+', 8, '+', 11, 12, '+'],
        ['+', 7, 9, 10, '+', '+'],
        ['+', 6, '+', '+', 2, '+'],
        ['+', 5, 4, 3, 1, '+'],
        ['+', '+', '+', '+', '+', '+']
    ]

def test_unsolvable_maze():
    maze = [
        ['+', '+', '+', '+', '+', '+'],
        ['+', ' ', '+', 'G', '+', '+'],
        ['+', ' ', '+', '+', '+', '+'],
        ['+', ' ', '+', '+', ' ', '+'],
        ['+', ' ', ' ', ' ', ' ', '+'],
        ['+', '+', '+', '+', '+', '+']
    ]
    
    assert solveMaze(maze, 4, 4) == False
